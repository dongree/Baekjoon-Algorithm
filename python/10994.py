n = int(input())

if n == 1:
    print('*')
else:
    print('*'*(4*n-3))
    for i in range(1, (4*n-3)//2):
        if i % 2 == 1:
            print('* '*(i//2+1)+' '*(4*(n-(i//2+1))-2)+'* '*(i//2+1))
        else:
            print('* '*(i//2)+'*'*(4*(n-i//2)-3)+' *'*(i//2))
    for i in range((4*n-3)//2, 0, -1):
        if i % 2 == 1:
            print('* '*(i//2+1)+' '*(4*(n-(i//2+1))-2)+'* '*(i//2+1))
        else:
            print('* '*(i//2)+'*'*(4*(n-i//2)-3)+' *'*(i//2))
    print('*'*(4*n-3))
